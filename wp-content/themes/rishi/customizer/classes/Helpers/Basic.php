	public static function ordered_keys( $choices, $args = [] ) {
		if ( isset( $choices[0] ) ) {
			return $choices;
		}

		$args = wp_parse_args(
			$args,
			[
				'additional' => []
			]
		);

		$result = [];

		foreach ( $choices as $key => $val ) {
			$result[] = array_merge( [
				'key'   => $key,
				'value' => $val,
			], $args['additional'] );
		}

		return $result;
	}

