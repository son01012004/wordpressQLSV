	public static function uniqid( $slug = null ) {
		if ( $slug ) {
			return 'uid_' . md5( $slug );
		}
		return 'uid_' . md5( time() . '-' . uniqid( \wp_rand(), true ) . '-' . \wp_rand() );
	}

